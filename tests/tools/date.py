from datetime import datetime, date


def to_proto_test_date(value: date) -> str:
    """
    Преобразует дату в строковое представление,
    используемое в protobuf-контрактах.

    Эта функция используется в gRPC-тестах и проверках,
    чтобы дата всегда передавалась и сравнивалась
    в ожидаемом контрактом формате.
    """
    return value.strftime('%Y-%m-%d')


def to_proto_test_datetime(value: datetime) -> str:
    """
    Преобразует дату и время в строковое представление,
    используемое в protobuf-контрактах.

    Наличие единой функции форматирования позволяет:
    - избежать расхождений в тестах,
    - централизованно менять формат при изменении контракта,
    - упростить проверки ответов gRPC API.
    """
    return value.strftime('%Y-%m-%d %H:%M:%S')
